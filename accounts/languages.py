def language_choices_for(saved_languages=None):
    choices = list(LANGUAGE_CHOICES)
    known = {code for code, _ in choices}
    for lang in saved_languages or []:
        if lang and lang not in known:
            choices.append((lang, lang))
            known.add(lang)
    return choices
